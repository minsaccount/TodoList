<template>
	<ul>
		<li :style="{ opacity }">欢迎学习vue!</li>
		<li>news001<input type="text" /></li>
		<li>news002<input type="text" /></li>
		<li>news003<input type="text" /></li>
	</ul>
</template>

<script>
export default {
	name: "HomeNews",
	data() {
		return {
			opacity: 1
		}
	},
	activated() {
		this.timer = setInterval(() => {
			console.log("@")
			this.opacity -= 0.01
			if (this.opacity <= 0) this.opacity = 1
		}, 16)
	},
	deactivated() {
		clearInterval(this.timer)
		console.log("定时器已关闭")
	}
}
</script>
